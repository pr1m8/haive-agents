# Example of a specific solution type - Equation (for Game of 24 etc.)
# ======================================================

# Type definitions for equation tokens
OperatorType = Literal["+", "-", "*", "/"]
TokenType = Union[float, OperatorType]


class Equation(BaseModel):
    """An equation in reverse-polish notation that combines numbers to reach a target value.

    This is one possible implementation of a solution type for mathematical problems.
    """

    tokens: list[TokenType] = Field(
        description="The stack of tokens and operators in reverse-polish notation. Example: [3, 4, '+', -1, '*'] would evaluate to (3 + 4) * -1 = -7."
    )

    model_config = ConfigDict(
        json_schema_extra={
            "examples": [
                {"tokens": [3.0, 4.0, "+", -1.0, "*"]},
                {"tokens": [7.0, 5.0, "*", 12.0, "/"]},
            ]
        }
    )

    def compute(self) -> float:
        """Compute the result of the equation."""
        op_funcs = {
            "+": operator.add,
            "-": operator.sub,
            "*": operator.mul,
            "/": operator.truediv,
        }
        stack = []
        for token in self.tokens:
            if isinstance(token, float):
                stack.append(token)
            else:
                b, a = stack.pop(), stack.pop()
                stack.append(op_funcs[token](a, b))

        return stack[0]

    def __str__(self) -> str:
        """String representation of the equation."""
        try:
            result = self.compute()
            return f"Equation({self.tokens}) = {result}"
        except Exception as e:
            return f"Equation({self.tokens}) [Error: {e}]"


class EquationGeneration(BaseModel):
    """Specialized candidate generation for equation problems.

    This is a structured output model for equation-specific generation.
    """

    reasoning: str = Field(description="The reasoning behind the generated equations")

    equations: list[Equation] = Field(
        description="List of equation solutions", min_items=1
    )

    explanations: Optional[List[str]] = Field(
        default=None, description="Optional explanations for each equation"
    )

    model_config = ConfigDict(
        arbitrary_types_allowed=True,
        json_schema_extra={
            "examples": [
                {
                    "reasoning": "I'll try different combinations of operations...",
                    "equations": [
                        {"tokens": [3.0, 4.0, "+", 8.0, "*"]},
                        {"tokens": [4.0, 8.0, "+", 3.0, "*"]},
                    ],
                    "explanations": [
                        "Using the numbers in RPN: 3 4 + 8 *",
                        "Grouping differently: 3 * (4 + 8)",
                    ],
                }
            ]
        })

    def to_candidates(self) -> list[Candidate[Equation]]:
        """Convert equations to Candidate objects."""
        candidates = []

        # If we have explanations, use them as metadata
        if self.explanations and len(self.explanations) == len(self.equations):
            for i, equation in enumerate(self.equations):
                candidates.append(
                    Candidate(
                        content=equation, metadata={"explanation": self.explanations[i]}
                    )
                )
        else:
            # Otherwise just use the equations
            candidates = [Candidate(content=eq) for eq in self.equations]

        return candidates

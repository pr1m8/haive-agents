"""Tree of Thoughts (ToT) models and data structures.

This module defines the core data models for the Tree of Thoughts reasoning algorithm,
including candidate solutions and structured output models.
"""

import operator
from typing import Any, Generic, Literal, TypeVar, Union, Optional, List

from pydantic import BaseModel, ConfigDict, Field

# Generic type for solution content
T = TypeVar("T")


class Candidate(BaseModel, Generic[T]):
    """A candidate solution in the Tree of Thoughts algorithm.

    This represents an unscored candidate solution generated by the LLM.
    """

    content: T = Field(description="The candidate solution content")
    metadata: dict[str, Any] = Field(
        default_factory=dict, description="Additional metadata"
    )

    # Use Pydantic v2 configuration
    model_config = ConfigDict(
        validate_assignment=True,
        extra="allow",
        arbitrary_types_allowed=True)

    def __str__(self) -> str:
        """String representation of the candidate."""
        content_str = str(self.content)
        content_preview = (
            content_str[:50] + "..." if len(content_str) > 50 else content_str
        )
        return f"Candidate(content='{content_preview}')"


class Score(BaseModel):
    """A score with feedback.

    This represents the evaluation of a candidate solution.
    """

    value: float = Field(description="Numerical score between 0 and 1", ge=0, le=1)
    feedback: Optional[str] = Field(
        default=None, description="Feedback explaining the score"
    )

    model_config = ConfigDict(
        validate_assignment=True)

    def __str__(self) -> str:
        """String representation of the score."""
        if self.feedback:
            feedback_preview = (
                self.feedback[:30] + "..." if len(self.feedback) > 30 else self.feedback
            )
            return f"Score({self.value}, feedback='{feedback_preview}')"
        return f"Score({self.value})"


class ScoredCandidate(BaseModel, Generic[T]):
    """A candidate solution with its evaluation score.

    This combines a candidate solution with its score and feedback.
    """

    candidate: Candidate[T] = Field(description="The candidate solution")
    score: Score = Field(description="The evaluation score")

    model_config = ConfigDict(
        validate_assignment=True,
        arbitrary_types_allowed=True)

    def __str__(self) -> str:
        """String representation of the scored candidate."""
        return f"ScoredCandidate({self.candidate}, score={self.score})"

    @property
    def content(self) -> T:
        """Get the content of the candidate."""
        return self.candidate.content

    @property
    def value(self) -> float:
        """Get the numerical score value."""
        return self.score.value

    @property
    def feedback(self) -> Optional[str]:
        """Get the feedback if any."""
        return self.score.feedback

    @property
    def metadata(self) -> dict[str, Any]:
        """Get the metadata of the candidate."""
        return self.candidate.metadata


# Helper functions for state management


def update_candidates(
    existing: Optional[List[Any]] = None, updates: Optional[Union[List[Any], str]] = None
) -> list[Any]:
    """Update candidate list, handling special cases like clearing.

    Args:
        existing: Current list of candidates
        updates: New candidates to add, or "clear" to empty the list

    Returns:
        Updated list of candidates
    """
    if existing is None:
        existing = []
    if updates is None:
        return existing
    if updates == "clear":
        return []
    # Concatenate the lists
    return existing + updates


